'use client';

import { createContext, useContext, useState, useEffect, useRef, ReactNode, startTransition } from 'react';
import translationsId from '@/locales/id.json';
import translationsEn from '@/locales/en.json';

type Language = 'id' | 'en';

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  isHydrated: boolean;
  isTransitioning: boolean;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

const translationsMap = {
  id: translationsId,
  en: translationsEn,
};

export function LanguageProvider({ children, initialLanguage = 'id' }: { children: ReactNode; initialLanguage?: Language }) {
  const [language, setLanguageState] = useState<Language>(initialLanguage);
  const [isHydrated, setIsHydrated] = useState(false);
  const [isTransitioning, setIsTransitioning] = useState(false);
  const hasHydratedRef = useRef(false);
  const initialLanguageRef = useRef<Language>(initialLanguage);

  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = language;
    }
  }, [language]);

  useEffect(() => {
    const savedLanguage = localStorage.getItem('language') as Language;
    if (savedLanguage && (savedLanguage === 'id' || savedLanguage === 'en')) {
      setLanguageState(savedLanguage);
      document.cookie = `language=${savedLanguage};path=/;max-age=31536000`;
    }
    hasHydratedRef.current = true;
    setIsHydrated(true);
  }, []);

  const setLanguage = (lang: Language) => {
    if (lang === language) return;
    
    setIsTransitioning(true);
    
    setTimeout(() => {
      if (hasHydratedRef.current) {
        startTransition(() => {
          setLanguageState(lang);
          localStorage.setItem('language', lang);
          document.cookie = `language=${lang};path=/;max-age=31536000`;
        });
      } else {
        setLanguageState(lang);
      }
      
      setTimeout(() => {
        setIsTransitioning(false);
      }, 150);
    }, 150);
  };

  const t = (key: string): string => {
    // Use initial language until hydration completes to prevent server/client mismatch
    const langToUse = isHydrated ? language : initialLanguageRef.current;
    const translations = translationsMap[langToUse];
    const keys = key.split('.');
    let value: any = translations;
    
    for (const k of keys) {
      if (value && typeof value === 'object') {
        value = value[k];
      } else {
        return key;
      }
    }
    
    return typeof value === 'string' ? value : key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, isHydrated, isTransitioning }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
